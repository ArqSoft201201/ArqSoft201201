CREATE TABLE TIPOFUNCIONARIO (
	CODTIPFUN INTEGER PRIMARY KEY, -- CODIGO DO TIPO DE FUNCIONARIO
	NOMTIPFUN VARCHAR(15) NOT NULL -- NOME DO TIPO DE FUNCIONARIO
);

CREATE TABLE FUNCIONARIO (
	CODFUN    INTEGER PRIMARY KEY,  -- CODIGO DO FUNCIONARIO
	NOMFUN    VARCHAR(35) NOT NULL, -- NOME DO FUNCIONARIO
	CODTIPFUN INTEGER NOT NULL,     -- CODIGO DO TIPO DE FUNCIONARIO
	EMAILFUN  VARCHAR(60) NOT NULL, -- EMAIL DO FUNCIONARIO.
	RGFUN     VARCHAR(10) NOT NULL, -- RG DO FUNCIONARIO
	CPFFUN    CHAR(11) NOT NULL,    -- CPF DO FUNCIONARIO
	NUMTELFUN CHAR(10)              -- NUMERO DE TELEFONE DO FUNCIONARIO
);

CREATE TABLE USUARIO (
	CODUSU     INTEGER PRIMARY KEY,  -- CODIGO DO USUARIO
	CODFUN     INTEGER NOT NULL,     -- CODIGO DO FUNCIONARIO
	LOGUSU     VARCHAR(10) NOT NULL, -- LOGIN DO USUARIO
	SENUSU     VARCHAR(40) NOT NULL, -- SENHA DO USUARIO
	IDSITUATIV CHAR(1) NOT NULL      -- ID DE SITUACAO DO USUARIO (ATIVO/INATIVO)
);

CREATE TABLE HORARIO (
	CODHOR INTEGER PRIMARY KEY, -- CODIGO DO HORARIO
	DIASEM CHAR(3) NOT NULL,    -- DIA DA SEMANA
	HORINI DATE,                -- HORA DE INICIO DO HORARIO
	HORFIM DATE                 -- HORA DE FIM DO HORARIO
);

CREATE TABLE TURMA (
	CODTUR INTEGER PRIMARY KEY,   -- CODIGO DA TURMA
	NOMTUR VARCHAR (10),          -- NOME DA TURMA
	CODDIS INTEGER NOT NULL,      -- CODIGO DA DISCIPLINA
	CODFUN INTEGER NOT NULL,      -- CODIGO DO FUNCIONARIO
	CODHOR INTEGER NOT NULL,      -- CODIGO DO HORARIO
	NUMSAL INTEGER,               -- NUMERO DA SALA
	NUMPRE INTEGER                -- NUMERO DO PREDIO.
);

CREATE TABLE DISCIPLINA (
	CODDIS INTEGER PRIMARY KEY,
	NOMDIS VARCHAR(30)
);

CREATE TABLE RECURSO (
	CODREC     INTEGER PRIMARY KEY,   -- CODIGO DO RECURSO
	NOMREC     VARCHAR(20) NOT NULL,  -- NOME DO RECURSO
	IDTIPREC   CHAR(1) NOT NULL,      -- ID DO TIPO DE RECURSO
	NOMMARREC  VARCHAR(20),           -- NOME DA MARCA DO RECURSO
	DESATRIREC VARCHAR(100),          -- DESCRICAO DOS ATRIBUTOS DO RECURSO
	NUMSAL     INTEGER,               -- NUMERO DA SALA
	NUMPRE     INTEGER,               -- NUMERO DO PREDIO.
	STATUSREC  CHAR(1)                -- STATUS DO RECURSO (DISPONIVEL/INDISPONIVEL)
 );

CREATE TABLE RESERVA (
	CODRES       INTEGER PRIMARY KEY, -- CODIGO DA RESERVA
	CODUSU       INTEGER NOT NULL,    -- CODIGO DO USUARIO
	CODREC       INTEGER NOT NULL,    -- CODIGO DO RECURSO
	DESOCO       VARCHAR(500),        -- DESCRICAO DE OCORRENCIAS
	STATUS       CHAR(1) NOT NULL,    -- STATUS DA RESERVA (PENDENTE/ATENDIDA/TERMINADA/CANCELADA)
	NUMPREUSO    INTEGER,             -- NUMERO DO PREDIO DE USO
	NUMSALUSO    INTEGER,             -- NUMERO DA SALA DE USO
	DATINIRES    DATE NOT NULL,       -- DATA DE INICIO DA RESERVA
	DATPREFIMRES DATE NOT NULL,       -- DATA DE PREVISAO DE DEVOLUCAO DA RESERVA
	DATFIMRES    DATE NOT NULL,       -- DATA DE FIM DA RESERVA
	CODHOR    INTEGER              -- CODIGO DO HORARIO DA RESERVA
);

